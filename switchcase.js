let starRating = 5

switch(starRating){
    case 1:
        console.log ('very bad');
        break;
    case 2: 
        console.log ('bad');
        break;
    case 3:
        console.log ('Average');
        break;
    case 4:
        console.log ('Good');
        break;
    case 5:
        console.log ('Excellent');
        break;
    default:
        console.log ('Please enter a valid number');
        break;
}